package pe.edu.pucp.softven.daoImp.util;

public class ReporteVendedoresParametros {

    private Integer año;
    private Integer mes;
    private Integer tiendaId;
    private Integer vendedorId;

    /**
     * @return the año
     */
    public Integer getAño() {
        return año;
    }

    /**
     * @param año the año to set
     */
    public void setAño(Integer año) {
        this.año = año;
    }

    /**
     * @return the mes
     */
    public Integer getMes() {
        return mes;
    }

    /**
     * @param mes the mes to set
     */
    public void setMes(Integer mes) {
        this.mes = mes;
    }

    /**
     * @return the tiendaId
     */
    public Integer getTiendaId() {
        return tiendaId;
    }

    /**
     * @param tiendaId the tiendaId to set
     */
    public void setTiendaId(Integer tiendaId) {
        this.tiendaId = tiendaId;
    }

    /**
     * @return the vendedorId
     */
    public Integer getVendedorId() {
        return vendedorId;
    }

    /**
     * @param vendedorId the vendedorId to set
     */
    public void setVendedorId(Integer vendedorId) {
        this.vendedorId = vendedorId;
    }

}
